package level1;

/*
 * This class will help you understanding what are methods and how to call them.
 */
public class Classes_Methods {

	/*
	 * This is a method named as myFirstProgram which consist of content
	 * inside the parantesis{} which will help performing actions
	 */
	public static void myFirstProgram(String name) {
		//An action which is been performed when myFirstProgram method is called 
		System.out.println("Welcome to Java from " + name);
		
	}
	
	//Main method which is the starting point of execution
	public static void main(String[] args) {
	
		//Calling process of a method with action inputs as required
		myFirstProgram("Abhresh Sugandhi");
		
		
	
	}

}
